	public void showTheFlight() {
		String query = "select * from flights  where id = ?";
		
		try {
			PreparedStatement preparedStatement = connection.prepareStatement(query);
			
                        preparedStatement.setInt(1, this.planeId);
			
			ResultSet resultSet = preparedStatement.executeQuery();
			while(resultSet.next()) { // shows the informations of the flight
				this.fromWhereArea.setText(resultSet.getString("fromWhere"));
				this.destinationArea.setText(resultSet.getString("destination"));
				this.dateArea.setText(resultSet.getString("date"));
				this.timeArea.setText(resultSet.getString("time"));
			}
		} catch (SQLException ex) {
			Logger.getLogger(UpdateFlight.class.getName()).log(Level.SEVERE, null, ex);
		}
	}
	
	public void updateTheFlight(String fromWhere, String destination, String date, String time) {
		String query = "update flights set fromWhere = ?, destination = ?, date = ?, time = ?  where id = ?";

		try {
			PreparedStatement preparedStatement = connection.prepareStatement(query);
			preparedStatement.setString(1, fromWhere);
			preparedStatement.setString(2, destination);
			preparedStatement.setString(3, date);
			preparedStatement.setString(4, time);
			preparedStatement.setInt(5, this.planeId);
			preparedStatement.executeUpdate();  // update the information of the customer with news

			JOptionPane.showMessageDialog(this, "The Flight is updated.");
			adminPanel.showFlights(); // refreshes the adminPanel object
			this.setVisible(false); // makes invisible the current object
		} catch(SQLDataException ex) {
			JOptionPane.showMessageDialog(this, "Adding The Flight is failed.\nSome formats are not acceptable.\nIt might be due to date or time.\nMake an entry like 1984-01-01 and 10:30.");
			showTheFlight();
		} catch (SQLException ex) {
			JOptionPane.showMessageDialog(this, "Adding The Flight is failed.");
			showTheFlight();
		}
	}

