        private void buyTheTicketButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buyTheTicketButtonActionPerformed
		boolean flag = true;

		for (Enumeration<AbstractButton> buttons = seatsButtonGroup.getElements(); buttons.hasMoreElements();) {
			AbstractButton button = buttons.nextElement();

			if(button.isSelected()) {
				flag = false;
			}
		}

		if(flag) { // shows an error if the seat is not selected
			JOptionPane.showMessageDialog(this, "Please select a seat.");
		}

		else {
			this.seatNumber = Integer.valueOf(this.seatsButtonGroup.getSelection().getActionCommand());

			PaymentPage paymentPage = new PaymentPage(this.planeId, this.customerId, seatNumber, this.buyTicket, this);
			paymentPage.setVisible(true); // opens a paymentPage
		}
        }//GEN-LAST:event_buyTheTicketButtonActionPerformed

