	public void setPrices() {
		String query = "select * from flights where id = ?";
		
		try {
			PreparedStatement preparedStatement = connection.prepareStatement(query);
			
                        preparedStatement.setInt(1, this.planeId);
			
                        ResultSet resultSet = preparedStatement.executeQuery();
			if(resultSet.next()) { // set prices for all types
				this.firstClassPrice.setText("$" + String.valueOf(resultSet.getInt("firstClassPrice")));
				this.premiumClassPrice.setText("$" + String.valueOf(resultSet.getInt("premiumClassPrice")));
				this.mainCabinPrice.setText("$" + String.valueOf(resultSet.getInt("mainCabinPrice")));
			}
		} catch (SQLException ex) {
			Logger.getLogger(PaymentPage.class.getName()).log(Level.SEVERE, null, ex);
		}
	}
	
	public void setRadioActionCommands() {
		int counter = 0; // sets names of radios to use later.
		
		for (Enumeration<AbstractButton> buttons = seatsButtonGroup.getElements(); buttons.hasMoreElements();) {
			counter++;
			AbstractButton button = buttons.nextElement();
			button.setActionCommand(Integer.toString(counter));
		}
	}
	
	public void setUnvisibleTakenSeats() {
		for (Enumeration<AbstractButton> buttons = seatsButtonGroup.getElements(); buttons.hasMoreElements();) {
			AbstractButton button = buttons.nextElement();
			
			if(checkSeatStatus(Integer.valueOf(button.getActionCommand()))) {
				button.setVisible(false); // makes the seat invisible if it is already taken
			}
		}
	}
	
	public boolean checkSeatStatus(int seatNumber) {
		String query = "select * from sold_tickets where planeId = ? and seatNumber = ?";
		
		try {
			PreparedStatement preparedStatement = connection.prepareStatement(query);
			
			preparedStatement.setInt(1, this.planeId);
			preparedStatement.setInt(2, seatNumber);
			
			ResultSet resultSet = preparedStatement.executeQuery();
			if(resultSet.next()) {
				return true; // returns true if there is a matching seat number.
			}
			return false;
		} catch (SQLException ex) {
			Logger.getLogger(ChooseSeat.class.getName()).log(Level.SEVERE, null, ex);
			return false;
		}
	}
		
